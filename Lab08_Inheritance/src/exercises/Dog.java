package exercises;

public class Dog extends Animal {

    // Constructor
    public Dog() {
        System.out.println("Dog object instantiated!");
        this.happy = -10;
    }

    // Unique feed method for the Dog class
    @Override
    public void feed() {
        this.eat += 5;
        this.happy += 1;
    }

    // Overrides animal toString
    @Override
    public String toString() {
        return "Dog has " + super.toString();
    }
}
