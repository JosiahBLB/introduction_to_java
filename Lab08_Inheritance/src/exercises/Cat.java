package exercises;

public class Cat extends Animal {

    // Constructor
    public Cat() {
        System.out.println("Cat object instantiated!");
        this.happy = -10;
    }

    // Unique feed method for the Cat class
    @Override
    public void feed() {
        this.eat += 1;
        this.happy += 10;
    }

    // Unique toString method for the Cat class
    @Override
    public String toString() {
        return "Cat has " + super.toString();
    }
}