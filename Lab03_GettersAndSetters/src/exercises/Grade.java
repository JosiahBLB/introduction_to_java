package exercises;

public class Grade {
	// Instance variable
	private int percentage;
	
	// Constructor
	public Grade(int percentage) {
		this.percentage = percentage;
	}
	
	// Setter
	public void setPercentage(int percentage) {
		this.percentage = percentage;
	}

	// Getter
	public int getPercentage() {
		return this.percentage;
	}
}
